import entry from '../dist/plugin-entry.js';
const defs = [];
await entry.register({ registerTool(def){ defs.push(def); }, registerCli(){} }, {});
const tool = defs.find(d => d.name === 'umg_envoy_block_library_molt_map_fragment');
if (!tool) throw new Error('tool missing');
const run = async (input) => JSON.parse((await tool.execute(input)).content[0].text);
const primary = await run({ neoblockId: 'primary.sample' });
if (!primary.ok) throw new Error('primary failed');
if (primary.moltMapFragment.provenance.sourcePath !== 'AI/NEOBLOCKS/sample/primary.sample.json') throw new Error('primary sourcePath drift');
if (primary.moltMapFragment.provenance.manifestPath !== 'AI/MANIFESTS/neoblock-library-index.json') throw new Error('primary manifestPath drift');
if (primary.moltMapFragment.provenance.loadedFrom !== 'single_shallow_target') throw new Error('primary loadedFrom drift');
if (primary.moltMapFragment.provenance.backfillStatus !== 'SOURCE_PATH_BACKFILLED_FROM_MANIFEST_ENTRY') throw new Error('primary backfillStatus drift');
const directive = await run({ neoblockId: 'directive.sample' });
if (directive.moltMapFragment.provenance.sourcePath !== 'AI/NEOBLOCKS/sample/directive.sample.json') throw new Error('directive sourcePath drift');
const trigger = await run({ neoblockId: 'trigger.sample' });
if (trigger.moltMapFragment.provenance.sourcePath !== 'AI/NEOBLOCKS/sample/trigger.sample.json') throw new Error('trigger sourcePath drift');
const explicit = await run({ sourcePath: 'AI/NEOBLOCKS/sample/primary.sample.json', manifestKind: 'neoblock' });
if (explicit.moltMapFragment.provenance.sourcePath !== 'AI/NEOBLOCKS/sample/primary.sample.json') throw new Error('explicit sourcePath drift');
if (explicit.moltMapFragment.provenance.backfillStatus !== 'SOURCE_PATH_PROVIDED_BY_QUERY') throw new Error('explicit backfillStatus drift');
if (primary.moltMapFragment.fragmentStatus !== 'MOLT_MAP_FRAGMENT_READY') throw new Error('primary fragmentStatus drift');
if (primary.moltMapFragment.moltMapField !== 'Primary') throw new Error('primary moltMapField drift');
if (directive.moltMapFragment.moltMapField !== 'Directive') throw new Error('directive moltMapField drift');
if (trigger.moltMapFragment.moltMapField !== 'Trigger') throw new Error('trigger moltMapField drift');
if (Object.prototype.hasOwnProperty.call(primary.moltMapFragment, 'Directive')) throw new Error('full map should not be rendered');
const forbidden = await run({ sourcePath: 'archive/sample-basic_minimal.json', manifestKind: 'public_curated_catalog' });
if (!forbidden.errors.some(e => e.code === 'HOLD_TARGET_FORBIDDEN')) throw new Error('forbidden hold failed');
const slv = await run({ sourcePath: 'SLV.OPERATOR.json', manifestKind: 'public_curated_catalog' });
if (!slv.errors.some(e => e.code === 'HOLD_TARGET_OUTSIDE_ALLOWLIST')) throw new Error('SLV hold failed');
const missing = await run({ neoblockId: 'missing.sample' });
if (!missing.errors.some(e => e.code === 'HOLD_MANIFEST_ENTRY_NOT_FOUND')) throw new Error('missing hold failed');
const badProjection = await run({ neoblockId: 'primary.sample', projectionFormat: 'nonsense' });
if (!badProjection.errors.some(e => e.code === 'HOLD_MOLT_MAP_FRAGMENT_PROJECTION_FORMAT_UNSUPPORTED')) throw new Error('bad projection hold failed');
const raw = await run({ neoblockId: 'primary.sample', includeRaw: true });
if (!raw.errors.some(e => e.code === 'HOLD_RAW_TARGET_DUMP_NOT_SUPPORTED')) throw new Error('raw hold failed');
if (primary.execution !== 'not_performed' || primary.directSource !== 'not_enabled') throw new Error('runtime safety drift');
console.log(JSON.stringify({ ok: true, primary, directive, trigger, explicit }, null, 2));
