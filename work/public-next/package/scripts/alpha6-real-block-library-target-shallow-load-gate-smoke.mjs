import entry from '../dist/plugin-entry.js';
const defs = [];
await entry.register({ registerTool(def){ defs.push(def); }, registerCli(){} }, {});
const status = defs.find(d => d.name === 'umg_envoy_block_library_status');
const indexTool = defs.find(d => d.name === 'umg_envoy_block_library_manifest_index');
const lookup = defs.find(d => d.name === 'umg_envoy_block_library_manifest_entry_lookup');
const gate = defs.find(d => d.name === 'umg_envoy_block_library_target_shallow_load_gate');
if (!status) throw new Error('status tool missing');
if (!indexTool) throw new Error('manifest index tool missing');
if (!lookup) throw new Error('manifest entry lookup tool missing');
if (!gate) throw new Error('target shallow load gate tool missing');
const run = async (input) => JSON.parse((await gate.execute(input)).content[0].text);
const primary = await run({ entryId: 'primary.sample', manifestKind: 'neoblock' });
if (primary.gate.decision !== 'ALLOW_SHALLOW_LOAD' || primary.gate.canShallowLoad !== true) throw new Error('primary gate failed');
if (primary.gate.payloadLoaded !== false || primary.gate.recursiveLoad !== false) throw new Error('primary load flags drift');
const directive = await run({ entryId: 'directive.sample', manifestKind: 'neoblock' });
if (directive.gate.decision !== 'ALLOW_SHALLOW_LOAD') throw new Error('directive gate failed');
const trigger = await run({ entryId: 'trigger.sample', manifestKind: 'neoblock' });
if (trigger.gate.decision !== 'ALLOW_SHALLOW_LOAD') throw new Error('trigger gate failed');
const forbidden = await run({ sourcePath: 'archive/sample-basic_minimal.json', manifestKind: 'public_curated_catalog' });
if (forbidden.gate.decision !== 'DENY_FORBIDDEN_TARGET') throw new Error('forbidden gate failed');
const slv = await run({ sourcePath: 'SLV.OPERATOR.json', manifestKind: 'public_curated_catalog' });
if (slv.gate.decision !== 'DENY_OUTSIDE_ALLOWLIST') throw new Error('SLV gate failed');
const persona = await run({ sourcePath: 'sleeve-neomagnetar-dynamic-persona-v1.json', manifestKind: 'public_curated_catalog' });
if (persona.gate.decision !== 'DENY_OUTSIDE_ALLOWLIST') throw new Error('persona gate failed');
const missing = await run({ entryId: 'missing.sample' });
if (missing.gate.decision !== 'DENY_ENTRY_NOT_FOUND') throw new Error('missing entry gate failed');
const noQuery = await run({});
if (noQuery.gate.decision !== 'DENY_QUERY_REQUIRED') throw new Error('missing query gate failed');
const badKind = await run({ entryId: 'primary.sample', manifestKind: 'nonsense' });
if (badKind.gate.decision !== 'DENY_UNSUPPORTED_MANIFEST_KIND') throw new Error('bad kind gate failed');
const recursive = await run({ entryId: 'primary.sample', manifestKind: 'neoblock', intendedLoadMode: 'recursive' });
if (recursive.gate.decision !== 'DENY_UNSUPPORTED_LOAD_MODE') throw new Error('recursive mode gate failed');
console.log(JSON.stringify({ ok: true, primary, directive, trigger, forbidden, slv, persona, missing, noQuery, badKind, recursive }, null, 2));
