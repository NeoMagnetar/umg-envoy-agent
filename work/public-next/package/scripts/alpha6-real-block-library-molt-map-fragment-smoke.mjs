import entry from '../dist/plugin-entry.js';
const defs = [];
await entry.register({ registerTool(def){ defs.push(def); }, registerCli(){} }, {});
for (const name of ['umg_envoy_block_library_status','umg_envoy_block_library_manifest_index','umg_envoy_block_library_manifest_entry_lookup','umg_envoy_block_library_target_shallow_load_gate','umg_envoy_block_library_target_shallow_load_single','umg_envoy_block_library_target_shallow_summary_normalize','umg_envoy_block_library_neoblock_inspect','umg_envoy_block_library_moltblock_visible_extract','umg_envoy_block_library_molt_map_fragment']) {
  if (!defs.find(d => d.name === name)) throw new Error(`${name} missing`);
}
const tool = defs.find(d => d.name === 'umg_envoy_block_library_molt_map_fragment');
const run = async (input) => JSON.parse((await tool.execute(input)).content[0].text);
const primary = await run({ neoblockId: 'primary.sample' });
if (!primary.ok) throw new Error('primary fragment failed');
if (primary.moltMapFragment.fragmentStatus !== 'MOLT_MAP_FRAGMENT_READY') throw new Error('primary fragmentStatus drift');
if (primary.moltMapFragment.moltType !== 'Primary') throw new Error('primary moltType drift');
if (primary.moltMapFragment.moltMapField !== 'Primary') throw new Error('primary moltMapField drift');
if (!primary.nlProjection.includes('Primary:')) throw new Error('primary NL projection drift');
if (primary.moltMapFragment.referenceSummary.resolvedRefs !== 0 || primary.moltMapFragment.referenceSummary.loadedRefs !== 0) throw new Error('primary refs should not resolve/load');
const directive = await run({ neoblockId: 'directive.sample' });
if (directive.moltMapFragment.moltMapField !== 'Directive') throw new Error('directive field drift');
if (!directive.nlProjection.includes('Directive:')) throw new Error('directive NL projection drift');
const trigger = await run({ neoblockId: 'trigger.sample' });
if (trigger.moltMapFragment.moltMapField !== 'Trigger') throw new Error('trigger field drift');
if (!trigger.nlProjection.includes('Trigger:')) throw new Error('trigger NL projection drift');
if (trigger.visibleMoltExtraction.triggerEvaluation !== 'not_performed') throw new Error('trigger evaluation drift');
if (Object.prototype.hasOwnProperty.call(trigger.moltMapFragment, 'Directive')) throw new Error('full map should not be rendered');
const forbidden = await run({ sourcePath: 'archive/sample-basic_minimal.json', manifestKind: 'public_curated_catalog' });
if (!forbidden.errors.some(e => e.code === 'HOLD_TARGET_FORBIDDEN')) throw new Error('forbidden hold failed');
const slv = await run({ sourcePath: 'SLV.OPERATOR.json', manifestKind: 'public_curated_catalog' });
if (!slv.errors.some(e => e.code === 'HOLD_TARGET_OUTSIDE_ALLOWLIST')) throw new Error('SLV hold failed');
const persona = await run({ sourcePath: 'sleeve-neomagnetar-dynamic-persona-v1.json', manifestKind: 'public_curated_catalog' });
if (!persona.errors.some(e => e.code === 'HOLD_TARGET_OUTSIDE_ALLOWLIST')) throw new Error('persona hold failed');
const missing = await run({ neoblockId: 'missing.sample' });
if (!missing.errors.some(e => e.code === 'HOLD_MANIFEST_ENTRY_NOT_FOUND')) throw new Error('missing hold failed');
const noQuery = await run({});
if (!noQuery.errors.some(e => e.code === 'HOLD_MOLT_MAP_FRAGMENT_QUERY_REQUIRED')) throw new Error('noQuery hold failed');
const badKind = await run({ neoblockId: 'primary.sample', manifestKind: 'nonsense' });
if (!badKind.errors.some(e => e.code === 'HOLD_MANIFEST_KIND_UNSUPPORTED')) throw new Error('bad kind hold failed');
const badProfile = await run({ neoblockId: 'primary.sample', summaryProfile: 'nonsense' });
if (!badProfile.errors.some(e => e.code === 'HOLD_SHALLOW_SUMMARY_PROFILE_UNSUPPORTED')) throw new Error('bad profile hold failed');
const badProjection = await run({ neoblockId: 'primary.sample', projectionFormat: 'nonsense' });
if (!badProjection.errors.some(e => e.code === 'HOLD_MOLT_MAP_FRAGMENT_PROJECTION_FORMAT_UNSUPPORTED')) throw new Error('bad projection hold failed');
const raw = await run({ neoblockId: 'primary.sample', includeRaw: true });
if (!raw.errors.some(e => e.code === 'HOLD_RAW_TARGET_DUMP_NOT_SUPPORTED')) throw new Error('raw hold failed');
console.log(JSON.stringify({ ok: true, primary, directive, trigger }, null, 2));
